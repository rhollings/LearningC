#include <stdio.h>
#include <math.h>

// Square Root
int main() {
  printf("%f", sqrt(16));
  return 0;
}

// Rounding
int main() {
  printf("%f\n", ceil(1.4));
  printf("%f\n", floor(1.4));
  return 0;
}

// To the Power of
int main() {
  printf("%f", pow(4, 3));
  return 0;
}

